package test;

public class SalesVO {
	String sid;
	String product;
	String product_price;
	String warranty;
	String product_from;
	String purchase_place;
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getProduct() {
		return product;
	}
	public void setProduct(String product) {
		this.product = product;
	}
	public String getProduct_price() {
		return product_price;
	}
	public void setProduct_price(String product_price) {
		this.product_price = product_price;
	}
	public String getWarranty() {
		return warranty;
	}
	public void setWarranty(String warranty) {
		this.warranty = warranty;
	}
	public String getProduct_from() {
		return product_from;
	}
	public void setProduct_from(String product_from) {
		this.product_from = product_from;
	}
	public String getPurchase_place() {
		return purchase_place;
	}
	public void setPurchase_place(String purchase_place) {
		this.purchase_place = purchase_place;
	}
	
	
}
